/*****************************************************************************
* CLASS: IIntegerDataPoint
* DESCRIPTION: Integer data point interface
*****************************************************************************/
class IIntegerDataPoint : public virtual IMinMaxDataPoint
{
protected:
  /*****************************************************************************
  * LIFECYCLE - Constructor
  *****************************************************************************/
  IIntegerDataPoint() 
  {
  }
  
public:
  /*****************************************************************************
  * LIFECYCLE - Destructor
  *****************************************************************************/
  virtual ~IIntegerDataPoint() 
  {
  }
	
public:  
  /*****************************************************************************
  * PUBLIC METHODS
  *****************************************************************************/
  
  /*****************************************************************************
  * FUNCTION - GetAsInt
  * DESCRIPTION: Returns the value as an int.
  *****************************************************************************/
	virtual int GetAsInt(void) = 0;
  
  /*****************************************************************************
  * FUNCTION - SetAsInt
  * DESCRIPTION: Sets the value as an int.
  * Returns true if observers was notified.
  *****************************************************************************/
	virtual bool SetAsInt(int intValue) = 0;

  /*****************************************************************************
  * FUNCTION - GetAsBool
  * DESCRIPTION: Returns the value as a bool.
  *****************************************************************************/
	virtual bool GetAsBool(void) = 0;
  
  /*****************************************************************************
  * FUNCTION - SetAsBool
  * DESCRIPTION: Sets the value as a bool.
  * Returns true if observers was notified.
  *****************************************************************************/
	virtual bool SetAsBool(bool boolValue) = 0;
};

