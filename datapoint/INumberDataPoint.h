/*****************************************************************************
* CLASS: INumberDataPoint
* DESCRIPTION: Number data point interface 
*****************************************************************************/
class INumberDataPoint : public virtual IMinMaxDataPoint
{
protected:
  /*****************************************************************************
  * LIFECYCLE - Constructor
  *****************************************************************************/
  INumberDataPoint() 
  {
  }
  
public:
  /*****************************************************************************
  * LIFECYCLE - Destructor
  *****************************************************************************/
  virtual ~INumberDataPoint() 
  {
  }

public:  
  /*****************************************************************************
  * PUBLIC METHODS
  *****************************************************************************/
  
  /*****************************************************************************
  * FUNCTION - IsFloat
  * DESCRIPTION: Returns true if the number data point is a float
  *****************************************************************************/
	virtual bool IsFloat(void) = 0;
  
  /*****************************************************************************
  * FUNCTION - GetAsInt
  * DESCRIPTION: Returns the value as an int.
  *****************************************************************************/
	virtual int GetAsInt(void) = 0;
  
  /*****************************************************************************
  * FUNCTION - SetAsInt
  * DESCRIPTION: Sets the value as an int.
  * Returns true if observers was notified.
  *****************************************************************************/
	virtual bool SetAsInt(int intValue) = 0;
	
  /*****************************************************************************
  * FUNCTION - GetAsFloat
  * DESCRIPTION: Returns the value as a float.
  *****************************************************************************/
	virtual float GetAsFloat(void) = 0;
  
  /*****************************************************************************
  * FUNCTION - SetAsFloat
  * DESCRIPTION: Sets the value as a float.
  * Returns true if observers was notified.
  *****************************************************************************/
	virtual bool SetAsFloat(float floatValue) = 0;
};

