#include <ISubject.h>
#include <IDataPoint.h>

/*****************************************************************************
DEFINES
*****************************************************************************/

/*****************************************************************************
TYPE DEFINES
*****************************************************************************/

/*****************************************************************************
* CLASS: IMinMaxDataPoint
* DESCRIPTION: 
*****************************************************************************/
class IMinMaxDataPoint : public virtual ISubject, public virtual IDataPoint
{
protected:
  /*****************************************************************************
  * LIFECYCLE - Constructor
  *****************************************************************************/
  IMinMaxDataPoint() 
  {
  }
  
public:
  /*****************************************************************************
  * LIFECYCLE - Destructor
  *****************************************************************************/
  virtual ~IMinMaxDataPoint() {};
  
public:
  /*****************************************************************************
  * PUBLIC METHODS
  *****************************************************************************/

  /*****************************************************************************
  * FUNCTION - IsAtMin
  * DESCRIPTION: Returns true if the data point value is <= min value
  *****************************************************************************/
	virtual bool IsAtMin(void) = 0;
  
  /*****************************************************************************
  * FUNCTION - IsAtMax
  * DESCRIPTION: Returns true if the data point value is >= max value
  *****************************************************************************/
	virtual bool IsAtMax(void) = 0;
  
  /*****************************************************************************
  * FUNCTION - GetMinAsInt
  * DESCRIPTION: Returns the min value as an int.
  *****************************************************************************/
	virtual int GetMinAsInt(void) = 0;
  
  /*****************************************************************************
  * FUNCTION - GetMaxAsInt
  * DESCRIPTION: Returns the max value as an int.
  *****************************************************************************/
	virtual int GetMaxAsInt(void) = 0;
  
  /*****************************************************************************
  * FUNCTION - GetMinAsFloat
  * DESCRIPTION: Returns the min value as a float.
  *****************************************************************************/
	virtual float GetMinAsFloat(void) = 0;
  
  /*****************************************************************************
  * FUNCTION - GetMaxAsFloat
  * DESCRIPTION: Returns the max value as a float.
  *****************************************************************************/
	virtual float GetMaxAsFloat(void) = 0;

  /*****************************************************************************
  * FUNCTION - SetMaxAsInt
  * DESCRIPTION: sets the max value as an int.
  *****************************************************************************/
	virtual bool SetMaxAsInt(int newValue) = 0;

  /*****************************************************************************
  * FUNCTION - SetMinAsInt
  * DESCRIPTION: sets the min value as an int.
  *****************************************************************************/
	virtual bool SetMinAsInt(int newValue) = 0;

};

#endif
