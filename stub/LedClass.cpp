// Constructor implementing - Initializing our ports and our state variable
LedClass::LedClass()
{
  SETUP_GPIO;
  state = 0;
}

void LedClass::ToggleLed(void)
{
  if ( state == 0 )
  {
    DB_PIN_ON;
    state = 1;
  }
  else
  {
    DB_PIN_OFF;
    state = 0;
  }
}

