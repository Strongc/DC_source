#ifndef __BLACK_BOARD_H__
#define __BLACK_BOARD_H__

/*****************************************************************************
SYSTEM INCLUDES
*****************************************************************************/
#include <vector>

/*****************************************************************************
PROJECT INCLUDES
*****************************************************************************/
#include <Component.h>

/*****************************************************************************
LOCAL INCLUDES
****************************************************************************/
#include <Chalk.h>

/*****************************************************************************
DEFINES
*****************************************************************************/

namespace mpc
{
  namespace display
  {

    /*****************************************************************************
    TYPE DEFINES
    *****************************************************************************/
    typedef std::vector<Chalk*>         CHALK_VECTOR_TYPE;
    typedef CHALK_VECTOR_TYPE::iterator CHALK_VECTOR_ITR;
    
    /*****************************************************************************
    * CLASS: BlackBoard
    * DESCRIPTION:
    * A blackboard for drawing chalk on.
    *****************************************************************************/
    class BlackBoard : public Component
    {
    public:
     /********************************************************************
      LIFECYCLE - Default constructor.
      ********************************************************************/
     BlackBoard(Component* pParent = NULL);
      
     /********************************************************************
      LIFECYCLE - Destructor.
      ********************************************************************/
     virtual ~BlackBoard();
     /********************************************************************
      ASSIGNMENT OPERATOR
      ********************************************************************/

     /********************************************************************
      OPERATIONS
      ********************************************************************/
      
      /*************************************************************************
      * Function - AddChalk
      * DESCRIPTION:
      * Adds the specified chalk to the blackboard
      *************************************************************************/
     void AddChalk(Chalk* pChalk);
      
     /*************************************************************************
      * Function - Redraw
      * DESCRIPTION:
      * Redraws this blackboard. If it fails (for some reason) the method returns
      * false.
      *************************************************************************/
     virtual bool Redraw();

      /*************************************************************************
      * Function - GetPointMargin
      * DESCRIPTION:
      * Returns a constant representing the blackboard margin to be accounted for 
      * when points are drawn.
      *************************************************************************/
     virtual const int GetPointMargin() {return 4;}

    private:
     /********************************************************************
      OPERATIONS
      ********************************************************************/
     /********************************************************************
      ATTRIBUTE
      ********************************************************************/
     CHALK_VECTOR_TYPE mChalks;    // all chalk added to the blackboard

    protected:
     /********************************************************************
      OPERATIONS
      ********************************************************************/

     /********************************************************************
      ATTRIBUTE
      ********************************************************************/

    }; // class BlackBoard

  } // namespace display
} // namespace mpc

#endif
